import { match } from '../src';
test('模式匹配', () => {
  expect(match.isPhoneNumber(null)).toBeFalsy();
  expect(match.isPhoneNumber(12345678)).toBeFalsy();
  expect(match.isPhoneNumber(11111111111)).toBeFalsy();
  expect(match.isPhoneNumber('11111111111')).toBeFalsy();
  expect(match.isPhoneNumber(13566666666)).toBeTruthy();
  expect(match.isPhoneNumber('13566666666')).toBeTruthy();
  expect(match.REG_PHONE.test('13566666666')).toBeTruthy();
  expect(match.isLandlinePhone(null)).toBeFalsy();
  expect(match.isLandlinePhone('99')).toBeFalsy();
  expect(match.isLandlinePhone('37679826148')).toBeTruthy();
  expect(match.REG_LANDLINE_PHONE.test('99')).toBeFalsy();
  expect(match.REG_LANDLINE_PHONE.test('37679826147')).toBeTruthy();
  expect(match.isIdCard(null)).toBeFalsy();
  expect(match.isIdCard('18815165656')).toBeFalsy();
  expect(match.isIdCard('440804199806131447')).toBeTruthy();
  expect(match.REG_ID_CARD.test('13566666666')).toBeFalsy();
  expect(match.REG_ID_CARD.test('440804199806131447')).toBeTruthy();
  expect(match.isPostcode(null)).toBeFalsy();
  expect(match.isPostcode('aa')).toBeFalsy();
  expect(match.isPostcode('520000')).toBeTruthy();
  expect(match.REG_POSTCODE.test('aa')).toBeFalsy();
  expect(match.REG_POSTCODE.test('520000')).toBeTruthy();
  expect(match.isEmail(null)).toBeFalsy();
  expect(match.isEmail('18815165656')).toBeFalsy();
  expect(match.isEmail('18815165656@qq.com')).toBeTruthy();
  expect(match.REG_EMAIL.test('18815165656')).toBeFalsy();
  expect(match.REG_EMAIL.test('18815165656@qq.com')).toBeTruthy();
  expect(match.isPureNumber('0')).toBeTruthy();
  expect(match.isPureNumber('0.5')).toBeTruthy();
  expect(match.isPureNumber('01.5')).toBeFalsy();
  expect(match.isPureNumber('10.5')).toBeTruthy();
  expect(match.isPureNumber(1.23)).toBeTruthy();
  expect(match.isPureNumber('-1.23')).toBeTruthy();
  expect(match.isPureNumber(NaN)).toBeFalsy();
  expect(match.isPureNumber(null)).toBeFalsy();
  expect(match.isPureNumber('-a.b')).toBeFalsy();
  expect(match.isPureNumber('0000')).toBeFalsy();
  expect(match.isPureNumber('-.1')).toBeFalsy();
  expect(match.isUrl('https://baidu.com/')).toBeTruthy();
  expect(match.isUrl('ws://baidu.com')).toBeFalsy();
  expect(match.isUrl('xxx//test')).toBeFalsy();
  expect(match.isPassword('12345678')).toBeTruthy();
  expect(match.isPassword('一二三四五六七八')).toBeFalsy();
});
